function PS:FormatMoney(amount)
	local amount = tonumber(amount) or 0
	local word = ' поинт'
	if amount > 4 and amount < 21 then
		word = word..'ов'
	else
		local lastDigit = amount % 10
		if lastDigit == 1 then
			word = word
		elseif lastDigit > 1 and lastDigit < 5 then
			word = word..'а'
		else
			word = word..'ов'
		end
	end

	return amount..word
end